package scan

import (
	"context"
	"testing"
)

func TestCargoDetector(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, dir, "Cargo.lock", `# This file is automatically @generated by Cargo.

[[package]]
name = "serde"
version = "1.0.210"
source = "registry+https://github.com/rust-lang/crates.io-index"

[[package]]
name = "tokio"
version = "1.40.0"
source = "registry+https://github.com/rust-lang/crates.io-index"

[metadata]
foo = "bar"
`)
	d := &cargoDetector{}
	out, err := d.Detect(context.Background(), dir)
	if err != nil {
		t.Fatal(err)
	}
	if len(out) != 2 {
		t.Fatalf("expected 2 cargo packages, got %d", len(out))
	}
	if out[0].Purl != "pkg:cargo/serde@1.0.210" {
		t.Errorf("unexpected first purl: %s", out[0].Purl)
	}
	if out[1].Version != "1.40.0" {
		t.Errorf("expected tokio 1.40.0, got %s", out[1].Version)
	}
}

